class neuralNetwork {
    constructor(layers) {
        this.weights = layers.slice(0,-1).map(function weightFun (sizeOfLayer, i) {
            //Мы представим веса, соединяющие каждый слой с предыдущим, в виде матрицы.
            // Каждый столбец этой матрицы весов будет представлять веса,
            // соединяющие все нейроны предыдущего слоя с одним нейроном следующего.
            const sizeOfNextLayer = layers[i+1]
            return ([...Array(sizeOfNextLayer)].map(function () {return ([...Array(sizeOfLayer)].map(w => math.random()))}));
        })
    }
    //При этом атрибут весов нашей сети - это список матриц. Каждая из матриц представляет все веса, соединяющие один слой с другим.


    //Функция обнаружения активации нейронов в выходном слое от любого заданного входного.
    feedForward (input) {
        let activations = input
        for (let layer of this.weights) {
            activations = layer.map(weights => {
                return sigmoid(math.dot(activations, weights))
            })
        }
        return activations
    }

    //Функция стоимости
    costFunction (input, output) {
        //Находим активации, которые у нас получаеются
        const activations = this.feedForward(input);
        //Найдем то на на сколько каждая активация отличиется от правильной. Возведем в квадрат
        const costVector = activations.map((ansActivation, index) => {
            return ( ansActivation - output[index] )**2
        })
        // Находим сумму
        return math.sum(costVector);
    }
    //Обратное распростронение
    backPropagation (input, output) {
        //
        let gradientVector = []
        const firstCost = this.costFunction(input, output)
        //
        this.weights.forEach((layer, layerIndex) => {
            const delLayer = []
            layer.forEach((neuron, neuronIndex) => {
                const delNeuron = []
                neuron.forEach((weight, weightIndex) => {
                    let firstValue = weight

                    this.weights[layerIndex][neuronIndex][weightIndex] += 0.01;
                    let newCost = this.costFunction(input, output)
                    let delWeight = (newCost - firstCost) / 0.01;
                    delNeuron.push(delWeight)
                    this.weights[layerIndex][neuronIndex][weightIndex] = firstValue
                })
                delLayer.push(delNeuron)
            })
            gradientVector.push(delLayer);
        })
        gradientVector.forEach((layer, layerIndex) => {
            layer.forEach((neuron, neuronIndex) => {
                neuron.forEach((del, weightIndex) => {
                    this.weights[layerIndex][neuronIndex][weightIndex] -= del;
                })
            })
        })
    };



    trainingOfNetwork ({ input, output, epochs }) {
        for (let n = 0; n < epochs; n++) {
            input.forEach((x, index) => {
                this.backPropagation(x, output[index]);
            });
        }
    }

    predict (x) {
        return this.feedForward(x);
    }
}
